# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution(object):
    def mergeInBetween(self, list1, a, b, list2):
        """
        :type list1: ListNode
        :type a: int
        :type b: int
        :type list2: ListNode
        :rtype: ListNode
        找到 a,b 对应的node,记录下来。在a.next = list2 , list2.next = b 
        """
        left = ListNode(None)
        right = ListNode(None)
        cnt = 0
        cur = list1 
        while cnt!= a-1:
            cur = cur.next 
            cnt += 1
        left = cur 
        cnt = 0
        cur = list1 
        while cnt != b+1:
            cur = cur.next
            cnt += 1 
        right = cur 

        left.next = list2 
        cur = list2 
        while cur.next:
            cur = cur.next 
        cur.next = right 
        return list1
